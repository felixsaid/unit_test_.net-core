using System;

namespace UnitTestingMath
{
    public class MathOperation
    {
        public static double AddNumbers(double first_number, double second_number)
        {
            return (first_number + second_number);
        }

        public static double SubtractNumbers(double first_number, double second_number)
        {
            return (first_number - second_number);
        }

        public static double MultiplyNumbers(double first_number, double second_number)
        {
            return (first_number * second_number);
        }

        public static double DivideNumbers(double first_number, double second_number)
        {
            return (first_number / second_number);
        }
    }
}
